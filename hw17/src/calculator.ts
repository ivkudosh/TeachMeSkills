export class Calculator {
    // eslint-disable-next-line @typescript-eslint/no-empty-function
    constructor() { }

    public getSumAddition(a: number, b: number) {
        return a + b;
    }

    public getSumMultiplication(a: number, b: number) {
        return a * b;
    }

    public getSumSubtraction(a: number, b: number) {
        return a - b;
    }

    public getSumDivision(a: number, b: number) {
        return a / b;
    }
}