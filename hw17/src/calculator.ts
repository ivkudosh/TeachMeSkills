export class Calculator {
    // eslint-disable-next-line @typescript-eslint/no-empty-function
    constructor() { }

    public getAddition(a: number, b: number) {
        return a + b;
    }

    public getMultiplication(a: number, b: number) {
        return a * b;
    }

    public getSubtraction(a: number, b: number) {
        return a - b;
    }

    public getDivision(a: number, b: number) {
        return a / b;
    }
}